#include "libft.h"

void		ft_clean_int_arr(int ***arr)
{
	int	i;
	int j;

	i = 0;
	j = 0;
	if (!(arr)[i])
		return ;
	while ((*arr)[i])
		i++;
	while (j < i)
	{
		free((*arr)[j]);
		j++;
	}
	free(*arr);
}
