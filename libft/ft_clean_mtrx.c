#include "libft.h"

void			ft_clean_mtrx(char ***mtrx)
{
	int			i;
	int			j;

	i = 0;
	j = 0;
	if (!(*mtrx)[i])
		return ;
	while ((*mtrx)[i])
		i++;
	while (j < i)
	{
		free((*mtrx)[j]);
		j++;
	}
	free(*mtrx);
}
